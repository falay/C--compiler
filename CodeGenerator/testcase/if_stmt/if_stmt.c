void main(){ 
	float a, b;
	
	read a;
	read b;
	
	if( a == b ) {
		print a;
		print " is equal to ";
		print b;
		print "\n";
	}
	
	if( a > b ) {
		print a;
		print " is greater than ";
		print b;
		print "\n";
	}
	
	else {
		print a;
		print " is less than or equal to ";
		print b;
		print "\n";
	}

}


/*
Input:
5.55
6.666

Output:
5.55 is less than or equal to 6.666
*/

